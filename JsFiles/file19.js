// switch statement

let day = 9;

switch(day){
  case 0:
    console.log("sunday");
    break;
  case 1:
    console.log("Monday");
    break;
  case 2: 
    console.log("tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("Thurday");
    break;  
  case 5:
    console.log("friday");
    break;
  case 6:
    console.log("Saturday");
    break;
  default:
    console.log("invalid input");
         
}