// block scope vs function scope;

// let and const are block scope
 // var is function scope
/**
 * we can access var from anywhere in the function and
 * outside of the function, but we can only access let and const 
 * in the same scope only or in lexical scope(parent scope);
 * hence "let" and "const" is the block scope and 
 * "var" is a function scope
 */


